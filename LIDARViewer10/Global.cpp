// This function returns the number of tokens in string.
int countTokens(char * strBuf)
{
	char strDeliminators[] = " ,\t\n";
	char * token;
	int	 nTokens = 0;	

	// Get the first token.
	token = strtok(strBuf, strDeliminators);
	if (token != NULL)
		nTokens++;

	while (strtok(NULL, strDeliminators) != NULL)
	{
		nTokens++;
	}
	
	return nTokens;
}

